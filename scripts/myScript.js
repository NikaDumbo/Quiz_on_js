class myQuiz{
  constructor(Question){
    this.myQuestionsArray = Question;
    this.questionSwitcher();
    this.questionDisplay();
    this.getFirstQuestion();

  }

  questionSwitcher(){
    let body = document.getElementsByTagName("body")[0];
    let container = document.createElement("div");
    container.setAttribute("class", "questionContainer");
    body.appendChild(container);
    this.myQuestionsArray.forEach((arr, index) => {
      let questionButton = document.createElement("button");
      let questionNumber = document.createTextNode("Question "+(index + 1));
      questionButton.setAttribute("id", "question"+(index+1));
      questionButton.setAttribute("class", "questionChoice");
      questionButton.setAttribute("onclick", "getQuestion("+index+")");
      questionButton.appendChild(questionNumber);
      container.appendChild(questionButton);
    });  
  }

  questionDisplay(){
    let body = document.getElementsByTagName("body")[0];
    let display = document.createElement("div");
    display.setAttribute("class", "displayContainer");
    body.appendChild(display);
    let question = document.createElement("div");
    question.setAttribute("id", "questionText");
    question.setAttribute("class", "questionText");
    display.appendChild(question);
    let functionalButtons = document.createElement("div");
   // question.setAttribute("id", "buttonContainer");
    functionalButtons.setAttribute("class", "buttonContainer");
    let finishButton = document.createElement("button");
    let finishButtonText = document.createTextNode("End Quiz");
    finishButton.setAttribute("onclick", "swal('Quiz finished successfully!', '', 'success')");
    finishButton.appendChild(finishButtonText);
    functionalButtons.appendChild(finishButton);
    let restartButton = document.createElement("button");
    let restartButtonText = document.createTextNode("Restart");
    restartButton.setAttribute("onclick", "location.reload()");
    restartButton.appendChild(restartButtonText);
    functionalButtons.appendChild(restartButton);
    display.appendChild(functionalButtons);

  }

  getFirstQuestion(){
    document.getElementById("questionText").innerHTML = Question[0].quest + "<br><br>"
  + "<input type='radio' id="+"answerA"+0+" name="+"answer"+0+">"+Question[0].answer[0] +" "
  + "<input type='radio' id="+"answerB"+0+" name="+"answer"+0+">"+Question[0].answer[1];

  let btn = document.getElementById("question"+(0+1));
  let radioA =  document.getElementById("answerA"+0);
  let radioB =  document.getElementById("answerB"+0);
  Question.forEach((arr, index) => {
    if(Question[index].flag[0] == false && Question[index].flag[1] == false)
      document.getElementById("question"+(index+1)).style.backgroundColor="transparent";
    if(Question[index].flag[0] == true || Question[index].flag[1] == true)
      document.getElementById("question"+(index+1)).style.backgroundColor="green";
  });
  btn.style.backgroundColor="#F26651";
  radioA.addEventListener('change', () => {btn.style.backgroundColor="green"; Question[0].flag[0] = true; Question[0].flag[1] = false;});
  radioB.addEventListener('change', () => {btn.style.backgroundColor="green"; Question[0].flag[1] = true; Question[0].flag[0] = false;});
  }

}

function getQuestion(questionNumber){
  document.getElementById("questionText").innerHTML = Question[questionNumber].quest + "<br><br>"
  + "<input type='radio' id="+"answerA"+questionNumber+" name="+"answer"+questionNumber+">"+Question[questionNumber].answer[0] +" "
  + "<input type='radio' id="+"answerB"+questionNumber+" name="+"answer"+questionNumber+">"+Question[questionNumber].answer[1];
  setButtonColor(questionNumber);
}

